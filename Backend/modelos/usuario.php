<?php

    class Usuario{
        //atributos
        private $conexion;

        public function __construct($conexion){
            $this->conexion = $conexion;
        }

        //Metodos

        public function consulta(){
            $sql = "SELECT * FORM usuario ORDER BY nombre";
            $res = mysqli_query($this->conexion, $sql) or die("No encontro la tabla USUARIO");

            $vec = [];

            while($row = mysql_fetch_array($res)){
                $vec[] = $row;                
            }

            return $vec;
        }

        public function eliminar($id){
            $sql = "DELETE FROM usurio WHERE id_usuario = $id";
            mysqli_query($this->conexion, $sql) or die("NO elimino el REGISTRO");

            $vec = [];
            $vec['Resultado'] = "OK";
            $vec['mensaje'] = "Se elimino el registro";

            return $vec;
        }

        public function insertar($params){
            $sql = "INSERT INTO usuario(nombre, email, clave, fo_pais, fo_ciudad, fo_permiso_nivel, fo_area_cargo) VALUES('$params->nombre', '$params->email', '$params->clave', $params->pais, $params->ciudad, $params->permiso_nivel, $params->area_cargo)";
            mysqli_query($this->conexion, $sql) or die("NO inserto el REGISTRO");

            $vec = [];
            $vec['Resultado'] = "OK";
            $vec['mensaje'] = "Se inserto el registro";

            return $vec;
        }

        public function editar($id, $params){
            $sql = "UPDATE usuario SET nombre = '$params->nombre' WHERE id_usuario = $id";
